require "test_helper"

class GithubReviewSubmitterTest < ActiveSupport::TestCase
  setup do
    @pr = PullRequest.create!(
      github_id: 12345,
      number: 42,
      title: "Test PR",
      url: "https://github.com/test/repo/pull/42",
      repo_owner: "test",
      repo_name: "repo",
      review_status: "pending_review"
    )
    @review_task = ReviewTask.create!(
      pull_request: @pr,
      state: "reviewed"
    )

    @comment1 = ReviewComment.create!(
      review_task: @review_task,
      body: "Critical issue",
      file_path: "app/models/user.rb",
      line_number: 10,
      severity: "critical",
      status: "pending"
    )

    @comment2 = ReviewComment.create!(
      review_task: @review_task,
      body: "Minor suggestion",
      file_path: "app/controllers/users_controller.rb",
      line_number: 25,
      severity: "minor",
      status: "pending"
    )

    @comment3 = ReviewComment.create!(
      review_task: @review_task,
      body: "General comment",
      file_path: "README.md",
      line_number: nil,
      severity: "suggestion",
      status: "pending"
    )

    @submitter = GithubReviewSubmitter.new(review_task: @review_task)
  end

  teardown do
    @pr&.destroy
    @review_task&.destroy
  end

  # validate_event! tests
  test "validate_event! accepts APPROVE" do
    assert_nothing_raised do
      @submitter.send(:validate_event!, "APPROVE")
    end
  end

  test "validate_event! accepts REQUEST_CHANGES" do
    assert_nothing_raised do
      @submitter.send(:validate_event!, "REQUEST_CHANGES")
    end
  end

  test "validate_event! accepts COMMENT" do
    assert_nothing_raised do
      @submitter.send(:validate_event!, "COMMENT")
    end
  end

  test "validate_event! raises Error for invalid event" do
    error = assert_raises(GithubReviewSubmitter::Error) do
      @submitter.send(:validate_event!, "INVALID")
    end

    assert_includes error.message, "Invalid review event"
    assert_includes error.message, "INVALID"
    assert_includes error.message, "APPROVE"
    assert_includes error.message, "REQUEST_CHANGES"
    assert_includes error.message, "COMMENT"
  end

  test "validate_event! is case sensitive" do
    assert_raises(GithubReviewSubmitter::Error) do
      @submitter.send(:validate_event!, "approve")
    end

    assert_raises(GithubReviewSubmitter::Error) do
      @submitter.send(:validate_event!, "Approve")
    end
  end

  # build_review_body tests
  test "build_review_body generates correct summary" do
    body = @submitter.send(:build_review_body, [ @comment1, @comment2 ])

    assert_includes body, "## Code Review Summary"
    assert_includes body, "Found **2** issues"
    assert_includes body, ":rotating_light:"
    assert_includes body, ":information_source:"
    assert_includes body, "Critical"
    assert_includes body, "Minor"
  end

  test "build_review_body includes footer" do
    body = @submitter.send(:build_review_body, [ @comment1 ])

    assert_includes body, "_Review generated by Forge_"
  end

  test "build_review_body uses custom summary when provided" do
    body = @submitter.send(:build_review_body, [ @comment1 ], "Custom summary")

    assert_equal "Custom summary", body
  end

  # determine_event tests
  test "determine_event returns REQUEST_CHANGES for critical issues" do
    event = @submitter.send(:determine_event, [ @comment1 ])
    assert_equal "REQUEST_CHANGES", event
  end

  test "determine_event returns REQUEST_CHANGES for major issues" do
    @comment1.update!(severity: "major")
    event = @submitter.send(:determine_event, [ @comment1 ])
    assert_equal "REQUEST_CHANGES", event
  end

  test "determine_event returns COMMENT for minor issues" do
    event = @submitter.send(:determine_event, [ @comment2 ])
    assert_equal "COMMENT", event
  end

  test "determine_event returns COMMENT for suggestion" do
    event = @submitter.send(:determine_event, [ @comment3 ])
    assert_equal "COMMENT", event
  end

  test "determine_event returns COMMENT when no critical/major issues" do
    event = @submitter.send(:determine_event, [ @comment2, @comment3 ])
    assert_equal "COMMENT", event
  end

  test "determine_event returns REQUEST_CHANGES with mixed severities" do
    event = @submitter.send(:determine_event, [ @comment1, @comment2, @comment3 ])
    assert_equal "REQUEST_CHANGES", event
  end

  # build_review_comments tests
  test "build_review_comments includes comments with file_path" do
    comments = @submitter.send(:build_review_comments, [ @comment1, @comment2, @comment3 ])

    assert_equal 3, comments.length
    assert_equal "app/models/user.rb", comments[0][:path]
    assert_equal "app/controllers/users_controller.rb", comments[1][:path]
    assert_equal "README.md", comments[2][:path]
  end

  test "build_review_comments includes line_number when present" do
    comments = @submitter.send(:build_review_comments, [ @comment1 ])

    assert_equal 10, comments[0][:line]
    assert_equal "RIGHT", comments[0][:side]
  end

  # build_comment_payload tests
  test "build_comment_payload includes all fields" do
    payload = @submitter.send(:build_comment_payload, @comment1)

    assert_equal "app/models/user.rb", payload[:path]
    assert_equal 10, payload[:line]
    assert_equal "RIGHT", payload[:side]
    assert_includes payload[:body], "Critical issue"
    assert_includes payload[:body], ":rotating_light:"
  end

  test "build_comment_payload omits line when line_number is nil" do
    payload = @submitter.send(:build_comment_payload, @comment3)

    assert_nil payload[:line]
    assert_nil payload[:side]
  end

  # format_user_friendly_error tests
  test "format_user_friendly_error trims whitespace" do
    error_msg = @submitter.send(:format_user_friendly_error, "  too   many   spaces  ")
    assert_equal "GitHub API error: too many spaces", error_msg
  end

  test "format_user_friendly_error truncates long messages" do
    long_msg = "x" * 300
    error_msg = @submitter.send(:format_user_friendly_error, long_msg)
    # Prefix "GitHub API error: " is 18 chars, message is truncated to 197 + "..." = 200
    # Total: 18 + 197 + 3 = 218 chars
    assert_equal 218, error_msg.length
    assert error_msg.end_with?("...")
  end

  test "format_user_friendly_error prefixes with GitHub API error" do
    error_msg = @submitter.send(:format_user_friendly_error, "something went wrong")
    assert_equal "GitHub API error: something went wrong", error_msg
  end

  # severity_emoji tests
  test "severity_emoji returns correct emoji for critical" do
    emoji = @submitter.send(:severity_emoji, "critical")
